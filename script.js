// Перемешиваем вопросы
function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

let shuffledQuestions = shuffleArray([...questions]);
let currentQuestionIndex = 0;
let userAnswers = new Array(shuffledQuestions.length).fill(null);

// Проверяем текущий вопрос
function checkCurrentQuestion() {
    const currentQuestion = shuffledQuestions[currentQuestionIndex];
    const userAnswer = userAnswers[currentQuestionIndex] || [];
    const correctOptions = currentQuestion.options
        .map((option, index) => ({ index, correct: option.correct }))
        .filter(option => option.correct)
        .map(option => option.index);

    // Сначала сбрасываем все классы
    const options = document.querySelectorAll('.option');
    options.forEach(option => {
        option.classList.remove('correct', 'incorrect', 'missed');
    });

    // Подсвечиваем правильные ответы, которые выбрал пользователь
    userAnswer.forEach(index => {
        const option = options[index];
        if (correctOptions.includes(index)) {
            option.classList.add('correct');
        } else {
            option.classList.add('incorrect');
        }
    });

    // Подсвечиваем пропущенные правильные ответы
    correctOptions.forEach(index => {
        if (!userAnswer.includes(index)) {
            options[index].classList.add('missed');
        }
    });
}

// Отображаем текущий вопрос
function displayQuestion() {
    const quizContainer = document.getElementById('quiz');
    const progressElement = document.getElementById('progress');
    const currentQuestion = shuffledQuestions[currentQuestionIndex];
    
    // Сбрасываем подсветку
    const options = document.querySelectorAll('.option');
    options.forEach(option => {
        option.classList.remove('correct', 'incorrect', 'missed');
    });

    progressElement.textContent = `Вопрос ${currentQuestionIndex + 1} из ${shuffledQuestions.length}`;
    
    let optionsHtml = '';
    currentQuestion.options.forEach((option, index) => {
        const isChecked = userAnswers[currentQuestionIndex]?.includes(index) ? 'checked' : '';
        optionsHtml += `
            <div class="option">
                <label>
                    <input type="checkbox" name="question${currentQuestionIndex}" value="${index}" ${isChecked} onchange="saveAnswer(${index})">
                    ${option.text}
                </label>
            </div>
        `;
    });

    quizContainer.innerHTML = `
        <div class="question">
            <p>${currentQuestion.question}</p>
            <div class="options">
                ${optionsHtml}
            </div>
        </div>
    `;

    // Добавляем обработчики клика для всей области варианта
    document.querySelectorAll('.option').forEach((option, index) => {
        option.addEventListener('click', (e) => {
            // Если клик был не по чекбоксу и не по label
            if (e.target.tagName !== 'INPUT' && e.target.tagName !== 'LABEL') {
                const checkbox = option.querySelector('input[type="checkbox"]');
                checkbox.checked = !checkbox.checked;
                saveAnswer(index);
            }
        });
    });

    document.getElementById('prev').disabled = currentQuestionIndex === 0;
    document.getElementById('next').disabled = currentQuestionIndex === shuffledQuestions.length - 1;
}

// Сохраняем ответ пользователя
function saveAnswer(optionIndex) {
    const checkboxes = document.querySelectorAll(`input[name="question${currentQuestionIndex}"]:checked`);
    userAnswers[currentQuestionIndex] = Array.from(checkboxes).map(cb => parseInt(cb.value));
}

// Переход к следующему вопросу
function nextQuestion() {
    if (currentQuestionIndex < shuffledQuestions.length - 1) {
        currentQuestionIndex++;
        displayQuestion();
    }
}

// Переход к предыдущему вопросу
function previousQuestion() {
    if (currentQuestionIndex > 0) {
        currentQuestionIndex--;
        displayQuestion();
    }
}

// Проверяем ответы
function checkAnswers() {
    let correctCount = 0;
    let wrongAnswers = [];

    shuffledQuestions.forEach((question, questionIndex) => {
        const userAnswer = userAnswers[questionIndex] || [];
        const correctOptions = question.options
            .map((option, index) => ({ index, correct: option.correct }))
            .filter(option => option.correct)
            .map(option => option.index);

        const isCorrect = userAnswer.length === correctOptions.length &&
            userAnswer.every(answer => correctOptions.includes(answer));

        if (isCorrect) {
            correctCount++;
        } else {
            wrongAnswers.push({
                question: question.question,
                userAnswer: userAnswer.map(index => question.options[index].text),
                correctAnswer: correctOptions.map(index => question.options[index].text)
            });
        }
    });

    const percentage = (correctCount / shuffledQuestions.length) * 100;
    const resultContainer = document.getElementById('result');
    resultContainer.innerHTML = `
        <h2>Результаты теста</h2>
        <p>Правильных ответов: ${correctCount} из ${shuffledQuestions.length} (${percentage.toFixed(1)}%)</p>
        ${wrongAnswers.length > 0 ? `
            <h3>Неправильные ответы:</h3>
            <div class="wrong-answers">
                ${wrongAnswers.map((item, index) => `
                    <div class="wrong-answer-item">
                        <p><strong>Вопрос ${index + 1}:</strong> ${item.question}</p>
                        <p><strong>Ваш ответ:</strong> ${item.userAnswer.join('; ') || '-'}</p>
                        <p><strong>Правильный ответ:</strong> ${item.correctAnswer.join('; ')}</p>
                    </div>
                `).join('')}
            </div>
        ` : ''}
        <button onclick="location.reload()">Начать заново</button>
    `;
    resultContainer.style.display = 'block';
    document.getElementById('quiz').style.display = 'none';
}

